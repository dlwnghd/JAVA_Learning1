package day02;

public class PrintTest {
	public static void main(String[] args) {
//		println() 메소드는 괄호안의 값을 출력하는 메소드
		/*
		 * 안녕
		 * 하세요
		 * 반가워요
		 */
		// 본인 이름 세글자 출력하기, 단 위치는
		// println 이라는 출력결과와 반갑습니다 출력 결과 사이에
		System.out.println("println");			//문자열
		System.out.println("이주홍");				//문자열
		System.out.println("반갑습니다");		//문자열
		
		System.out.println("안녕하세요");		//문자열
		System.out.println("123");					//문자열
		System.out.println(123);						//정수형
		System.out.println(0.5);						//실수형
		System.out.println('a');						//문자형	
	}
}
